def try_log_to_mlflow(
    metrics: dict[str, Any],
    tracking_uri: str,
    experiment_name: str,
    run_name: str,
    skip_mlflow: bool = False,
) -> dict[str, Any]:
    """Log existing metrics and artifacts to MLflow when available."""
    if skip_mlflow:
        return {
            "status": "skipped_by_user",
            "tracking_uri": tracking_uri,
            "experiment_name": experiment_name,
            "run_id": None,
        }

    try:
        import mlflow
    except ModuleNotFoundError:
        return {
            "status": "mlflow_not_installed",
            "tracking_uri": tracking_uri,
            "experiment_name": experiment_name,
            "run_id": None,
        }

    try:
        if tracking_uri.startswith("sqlite:///"):
            db_path = Path(tracking_uri.removeprefix("sqlite:///"))
            db_path.parent.mkdir(parents=True, exist_ok=True)

        mlflow.set_tracking_uri(tracking_uri)
        mlflow.set_experiment(experiment_name)
        with mlflow.start_run(run_name=run_name) as run:
            mlflow.set_tag("project", "FinSight")
            mlflow.set_tag("model_stage", "portfolio_ready_not_production_approved")
            mlflow.set_tag("no_retraining", "true")
            mlflow.log_param("model_type", metrics.get("model_type"))
            mlflow.log_param("selection_metric", metrics.get("selection_metric"))
            mlflow.log_param("threshold_strategy", metrics.get("threshold_strategy"))
            mlflow.log_param("feature_count", metrics.get("feature_count"))
            mlflow.log_param("rows_loaded", metrics.get("rows_loaded"))
            for param_name, value in metrics.get("best_params", {}).items():
                mlflow.log_param(f"best_{param_name}", value)
            for metric_name, value in flatten_metrics(metrics).items():
                mlflow.log_metric(metric_name, value)
            for artifact_path in ARTIFACT_PATHS:
                if artifact_path.exists():
                    mlflow.log_artifact(
                        artifact_path.as_posix(), artifact_path="reports"
                    )
            return {
                "status": "logged",
                "tracking_uri": tracking_uri,
                "experiment_name": experiment_name,
                "run_id": run.info.run_id,
            }
    except Exception as exc:
        return {
            "status": "mlflow_logging_failed",
            "tracking_uri": tracking_uri,
            "experiment_name": experiment_name,
            "run_id": None,
            "error": f"{type(exc).__name__}: {exc}",
        }


def write_mlflow_summary(entry: dict[str, Any]) -> None:
    """Write human-readable MLflow experiment summary."""
    mlflow_status = entry["mlflow"]
    status = mlflow_status.get("status")
    summary = [
        "# MLflow Experiment Summary",
        "",
        "This report documents MLflow tracking for the existing FinSight final "
        "model artifacts. The script does not train or retrain any model.",
        "",
        "## Status",
        "",
        markdown_table(
            [
                ("status", status),
                ("tracking_uri", mlflow_status.get("tracking_uri")),
                ("experiment_name", mlflow_status.get("experiment_name")),
                ("run_id", mlflow_status.get("run_id")),
            ]
        ),
        "",
        "## Logged Content",
        "",
        "- Final model parameters from `reports/final_model_metrics.json`",
        "- Validation and test classification metrics",
        "- Validation and test Brier scores",
        "- Final model report, calibration report, cross-validation summary, "
        "business impact summary, explainability summary, fairness/proxy-risk "
        "analysis, leakage audit, and model card when present",
        "",
        "## How To Run",
        "",
        "```bash",
        "python src/models/mlflow_tracking.py",
        "```",
        "",
        "Open the local MLflow UI after logging:",
        "",
        "```bash",
        "mlflow ui --backend-store-uri sqlite:///mlruns/mlflow.db",
        "```",
        "",
    ]
    if status == "mlflow_not_installed":
        summary.extend(
            [
                "## Local Environment Note",
                "",
                "MLflow is not installed in the current environment, so the script "
                "wrote registry documentation but did not create an MLflow run. "
                "Install project requirements and rerun the command to create the "
                "local SQLite `mlruns/mlflow.db` tracking store.",
                "",
            ]
        )
    if status == "mlflow_logging_failed":
        summary.extend(
            [
                "## MLflow Logging Error",
                "",
                "MLflow is installed, but the local logging attempt failed. The "
                "script still wrote registry documentation so the project remains "
                "reviewable.",
                "",
                f"- Error: `{mlflow_status.get('error')}`",
                "",
            ]
        )
    summary.extend(
        [
            "## Registry Outputs",
            "",
            "- `reports/model_registry.json`",
            "- `reports/model_registry.md`",
            "- `reports/mlflow_experiment_summary.md`",
            "",
            "## Production Interpretation",
            "",
            "This adds experiment tracking and registry-style documentation for "
            "portfolio reproducibility. A real production model registry would "
            "also include approval stages, access control, artifact retention, "
            "deployment links, rollback metadata, and model owner sign-off.",
            "",
        ]
    )
    MLFLOW_SUMMARY_PATH.write_text("\n".join(summary), encoding="utf-8")


def run(
    tracking_uri: str = MLFLOW_TRACKING_URI,
    experiment_name: str = EXPERIMENT_NAME,
    run_name: str = RUN_NAME,
    skip_mlflow: bool = False,
) -> dict[str, Any]:
    """Create MLflow run when possible and write registry docs."""
    metrics = load_metrics()
    mlflow_status = try_log_to_mlflow(
        metrics=metrics,
        tracking_uri=tracking_uri,
        experiment_name=experiment_name,
        run_name=run_name,
        skip_mlflow=skip_mlflow,
    )
    entry = registry_entry(metrics, mlflow_status)
    write_registry_docs(entry)
    write_mlflow_summary(entry)
    return entry


def main() -> None:
    """CLI entry point."""
    args = parse_args()
    entry = run(
        tracking_uri=args.tracking_uri,
        experiment_name=args.experiment_name,
        run_name=args.run_name,
        skip_mlflow=args.skip_mlflow,
    )
    print("MLflow tracking documentation complete.")
    print(f"MLflow status: {entry['mlflow']['status']}")
    print(f"Registry JSON saved to: {REGISTRY_JSON_PATH}")
    print(f"Registry report saved to: {REGISTRY_MD_PATH}")
    print(f"MLflow summary saved to: {MLFLOW_SUMMARY_PATH}")


if __name__ == "__main__":
    main()
