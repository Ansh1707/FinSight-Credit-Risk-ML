"""Log existing model artifacts to MLflow and create registry-style docs.

Run from the project root:
    python src/models/mlflow_tracking.py

This script does not train or retrain a model. It reads existing saved metrics
and artifacts, logs them to MLflow when MLflow is installed, and always writes
portfolio-friendly model registry documentation under reports/.
"""

from __future__ import annotations

import argparse
import json
from datetime import datetime, timezone
from pathlib import Path
from typing import Any


REPORTS_DIR = Path("reports")
MODEL_PATH = Path("models/credit_risk_model.pkl")
METRICS_PATH = REPORTS_DIR / "final_model_metrics.json"
MLFLOW_SUMMARY_PATH = REPORTS_DIR / "mlflow_experiment_summary.md"
REGISTRY_JSON_PATH = REPORTS_DIR / "model_registry.json"
REGISTRY_MD_PATH = REPORTS_DIR / "model_registry.md"
MLFLOW_TRACKING_URI = "sqlite:///mlruns/mlflow.db"
EXPERIMENT_NAME = "FinSight Credit Risk"
RUN_NAME = "final_lightgbm_existing_artifacts"

ARTIFACT_PATHS = [
    REPORTS_DIR / "final_model_report.md",
    REPORTS_DIR / "final_model_metrics.json",
    REPORTS_DIR / "cross_validation_summary.md",
    REPORTS_DIR / "calibration_report.md",
    REPORTS_DIR / "business_impact_summary.md",
    REPORTS_DIR / "explainability_summary.md",
    REPORTS_DIR / "fairness_proxy_analysis.md",
    REPORTS_DIR / "leakage_audit.md",
    REPORTS_DIR / "model_card.md",
]


def parse_args() -> argparse.Namespace:
    """Parse CLI arguments."""
    parser = argparse.ArgumentParser(
        description="Log existing FinSight model artifacts to MLflow."
    )
    parser.add_argument(
        "--tracking-uri",
        default=MLFLOW_TRACKING_URI,
        help=(
            "MLflow tracking URI. Defaults to local SQLite store "
            "sqlite:///mlruns/mlflow.db."
        ),
    )
    parser.add_argument(
        "--experiment-name",
        default=EXPERIMENT_NAME,
        help="MLflow experiment name.",
    )
    parser.add_argument(
        "--run-name",
        default=RUN_NAME,
        help="MLflow run name.",
    )
    parser.add_argument(
        "--skip-mlflow",
        action="store_true",
        help="Only write registry documentation; do not attempt MLflow logging.",
    )
    return parser.parse_args()


def load_metrics() -> dict[str, Any]:
    """Load final model metrics generated by train_final_model.py."""
    if not METRICS_PATH.exists():
        raise FileNotFoundError(
            "Missing reports/final_model_metrics.json. "
            "Run python src/models/train_final_model.py first."
        )
    return json.loads(METRICS_PATH.read_text(encoding="utf-8"))


def flatten_metrics(metrics: dict[str, Any]) -> dict[str, float]:
    """Flatten validation/test metrics for MLflow logging."""
    flattened: dict[str, float] = {}
    for split_name, split_payload in metrics.get("splits", {}).items():
        classification = split_payload.get("classification", {})
        calibration = split_payload.get("calibration", {})
        for metric_name, value in classification.items():
            if isinstance(value, (int, float)):
                flattened[f"{split_name}_{metric_name}"] = float(value)
        brier_score = calibration.get("brier_score")
        if isinstance(brier_score, (int, float)):
            flattened[f"{split_name}_brier_score"] = float(brier_score)
    return flattened


def registry_entry(metrics: dict[str, Any], mlflow_status: dict[str, Any]) -> dict[str, Any]:
    """Build a model registry-style metadata record from existing artifacts."""
    return {
        "model_name": "FinSight Credit Risk LightGBM",
        "model_version": "0.13.0",
        "model_stage": "portfolio_ready_not_production_approved",
        "model_type": metrics.get("model_type", "unknown"),
        "selection_metric": metrics.get("selection_metric", "unknown"),
        "threshold_strategy": metrics.get("threshold_strategy", "unknown"),
        "threshold": metrics.get("threshold"),
        "top_k": metrics.get("top_k"),
        "random_state": metrics.get("random_state"),
        "rows_loaded": metrics.get("rows_loaded"),
        "feature_count": metrics.get("feature_count"),
        "artifact_path": MODEL_PATH.as_posix(),
        "metrics_path": METRICS_PATH.as_posix(),
        "created_utc": datetime.now(timezone.utc).isoformat(),
        "mlflow": mlflow_status,
        "validation_metrics": metrics.get("splits", {})
        .get("validation", {})
        .get("classification", {}),
        "test_metrics": metrics.get("splits", {}).get("test", {}).get("classification", {}),
        "governance": {
            "model_card": "reports/model_card.md",
            "leakage_audit": "reports/leakage_audit.md",
            "proxy_risk_review": "reports/fairness_proxy_analysis.md",
            "governance_checklist": "reports/governance_checklist.md",
            "production_status": "Portfolio prototype; not production-approved.",
        },
    }


def markdown_table(rows: list[tuple[str, Any]]) -> str:
    """Render two-column markdown table."""
    table = ["| field | value |", "| --- | --- |"]
    for key, value in rows:
        table.append(f"| {key} | `{value}` |")
    return "\n".join(table)


